    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data = [];
        if (\Auth::check()) {
            $user = \Auth::user();
            $samples = $user->samples()->orderBy('created_at', 'desc');
            
            $data = [
                'user' => $user,
                'samples' => $samples,
            ];
            
        }
        
        
        return view('samples.index', $data);
    }

